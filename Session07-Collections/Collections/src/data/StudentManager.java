package data;

/**
 *
 * @author Admin
 */
public class StudentManager {
    private String id;
    private String name;
    private int yob;
    private double gpa;

    public StudentManager(String id, String name, int yob, double gpa) {
        this.id = id;
        this.name = name;
        this.yob = yob;
        this.gpa = gpa;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYob() {
        return yob;
    }

    public void setYob(int yob) {
        this.yob = yob;
    }

    public double getGpa() {
        return gpa;
    }

    public void setGpa(double gpa) {
        this.gpa = gpa;
    }
    
    public void show() {
        System.out.printf("|%20s|%20s|%d|%.2f|\n", id, name, yob, gpa);
    }
}
