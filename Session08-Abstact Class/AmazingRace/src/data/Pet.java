package data;

/**
 *
 * @author Admin
 */
public abstract class Pet {

    protected String name;
    protected int yob;
    protected double weight;

    public Pet(String name, int yob, double weight) {
        this.name = name;
        this.yob = yob;
        this.weight = weight;
    }

    public Pet() {
    }
    
    public abstract double run();
    public abstract void show();
}
